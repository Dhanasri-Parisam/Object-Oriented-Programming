// ------------------------------------------------------------
// SECTION 1: WHAT IS A CONSTRUCTOR?
// ------------------------------------------------------------
// Interview-ready definition:
// A constructor is a special member of a class that runs automatically
// when an object is created. Its main purpose is to initialize object state.
//
// Key points:
// 1) Constructor name must match class name.
// 2) Constructor has no return type (not even void).
// 3) Called automatically by Java at object creation time.

// ------------------------------------------------------------
// SECTION 2: PROBLEM WITHOUT CONSTRUCTOR
// ------------------------------------------------------------
class CarWithoutConstructor {
    String carName;
    int price;
}

class ProblemWithoutConstructorDemo {
    public static void main(String[] args) {
        // Object 1
        CarWithoutConstructor car1 = new CarWithoutConstructor();
        car1.carName = "F1";
        car1.price = 100;

        // Object 2
        CarWithoutConstructor car2 = new CarWithoutConstructor();
        car2.carName = "BMW";
        car2.price = 200;

        // If we create many objects, manual assignment repeats everywhere.
        // Problems:
        // - Repeated code
        // - Easy to forget initialization
        // - Lower readability
        // - Harder maintenance

        System.out.println(car1.carName + " : " + car1.price);
        System.out.println(car2.carName + " : " + car2.price);
    }
}

// ------------------------------------------------------------
// SECTION 3: SOLUTION USING CONSTRUCTOR
// ------------------------------------------------------------
class CarWithConstructor {
    String carName;
    int price;

    // Parameterized constructor initializes fields immediately.
    CarWithConstructor(String carName, int price) {
        this.carName = carName;
        this.price = price;
    }
}

class ConstructorSolutionDemo {
    public static void main(String[] args) {
        CarWithConstructor car1 = new CarWithConstructor("F1", 100);
        CarWithConstructor car2 = new CarWithConstructor("BMW", 200);

        System.out.println(car1.carName + " : " + car1.price);
        System.out.println(car2.carName + " : " + car2.price);

        // Cleaner than creating object then assigning fields manually.
    }
}

// ------------------------------------------------------------
// SECTION 4: CLEAN COMPARISON
// ------------------------------------------------------------
class CleanComparison {
    public static void main(String[] args) {
        // Without constructor style
        CarWithoutConstructor c1 = new CarWithoutConstructor();
        c1.carName = "BMW";
        c1.price = 200;

        // With constructor style
        CarWithConstructor c2 = new CarWithConstructor("BMW", 200);

        System.out.println("Without constructor -> " + c1.carName + " " + c1.price);
        System.out.println("With constructor    -> " + c2.carName + " " + c2.price);
    }
}

// ------------------------------------------------------------
// SECTION 5: TYPES OF CONSTRUCTORS
// ------------------------------------------------------------
class ConstructorTypes {
    int value;
    String label;

    // 1) No-arg constructor (user-defined)
    ConstructorTypes() {
        value = 0;
        label = "No-arg";
    }

    // 2) Parameterized constructor
    ConstructorTypes(int value, String label) {
        this.value = value;
        this.label = label;
    }

    void print() {
        System.out.println(label + " : " + value);
    }

    public static void main(String[] args) {
        ConstructorTypes a = new ConstructorTypes();
        ConstructorTypes b = new ConstructorTypes(99, "Parameterized");
        a.print();
        b.print();
    }
}

// Important note:
// Default constructor is generated by Java only if you do not write any
// constructor in the class. Once you write at least one constructor,
// Java will not auto-generate the default constructor.

// ------------------------------------------------------------
// SECTION 6: CONSTRUCTOR OVERLOADING
// ------------------------------------------------------------
class Student {
    int id;
    String name;
    double marks;

    Student() {
        this.id = 0;
        this.name = "Unknown";
        this.marks = 0.0;
    }

    Student(int id, String name) {
        this.id = id;
        this.name = name;
        this.marks = 0.0;
    }

    Student(int id, String name, double marks) {
        this.id = id;
        this.name = name;
        this.marks = marks;
    }

    void show() {
        System.out.println(id + " " + name + " " + marks);
    }
}

class ConstructorOverloadingDemo {
    public static void main(String[] args) {
        Student s1 = new Student();
        Student s2 = new Student(101, "Asha");
        Student s3 = new Student(102, "Rahul", 88.5);

        s1.show();
        s2.show();
        s3.show();
    }
}

// ------------------------------------------------------------
// SECTION 7: THE this KEYWORD (GOLDEN RULE)
// ------------------------------------------------------------
// this -> current object
//
// Main uses in interview:
// 1) Access current object fields
// 2) Call current class method
// 3) Call another constructor using this()
// 4) Pass current object as argument
// 5) Return current object

// ------------------------------------------------------------
// SECTION 8: this FOR FIELD VS PARAMETER NAME CONFLICT
// ------------------------------------------------------------
class CarThisField {
    String carName;

    CarThisField(String carName) {
        // Left side is instance variable, right side is parameter.
        this.carName = carName;
    }

    void display() {
        System.out.println(carName);
    }

    public static void main(String[] args) {
        CarThisField c = new CarThisField("Audi");
        c.display();
        // Output: Audi
    }
}

// ------------------------------------------------------------
// SECTION 9: this TO CALL CURRENT CLASS METHOD
// ------------------------------------------------------------
class CarThisMethod {
    void start() {
        System.out.println("Car Started");
    }

    void drive() {
        this.start();
        System.out.println("Car Driving");
    }

    public static void main(String[] args) {
        CarThisMethod c = new CarThisMethod();
        c.drive();
    }
}

// ------------------------------------------------------------
// SECTION 10: this() FOR CONSTRUCTOR CHAINING
// ------------------------------------------------------------
class CarThisConstructor {
    String name;
    int price;

    CarThisConstructor() {
        // Calls parameterized constructor first.
        this("BMW", 5000000);
    }

    CarThisConstructor(String name, int price) {
        this.name = name;
        this.price = price;
    }

    void show() {
        System.out.println(name + " " + price);
    }

    public static void main(String[] args) {
        CarThisConstructor c = new CarThisConstructor();
        c.show();
        // Output: BMW 5000000
    }
}

// ------------------------------------------------------------
// SECTION 11: this AS METHOD ARGUMENT
// ------------------------------------------------------------
class ThisAsArgument {
    void show(ThisAsArgument t) {
        if (t == this) {
            System.out.println("Same current object passed");
        }
    }

    void display() {
        show(this);
    }

    public static void main(String[] args) {
        ThisAsArgument obj = new ThisAsArgument();
        obj.display();
    }
}

// ------------------------------------------------------------
// SECTION 12: this AS RETURN VALUE (METHOD CHAINING IDEA)
// ------------------------------------------------------------
class ThisAsReturn {
    int count = 0;

    ThisAsReturn increment() {
        count++;
        return this;
    }

    void show() {
        System.out.println("count = " + count);
    }

    public static void main(String[] args) {
        ThisAsReturn t = new ThisAsReturn();
        t.increment().increment().increment().show();
        // Output: count = 3
    }
}

