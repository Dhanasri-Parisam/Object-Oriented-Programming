// ------------------------------------------------------------
// SECTION 20: INTERVIEW ONE-LINERS
// ------------------------------------------------------------
// Q: What is a constructor?
// A: Special class member used to initialize objects, auto-called at creation.
//
// Q: Why use constructor?
// A: To ensure object starts in valid state and reduce repeated setup code.
//
// Q: When is this used?
// A: this refers to current object, especially for field/parameter conflict
//    and constructor chaining.
//
// Q: Difference between method and constructor?
// A: Constructor has class name, no return type, runs automatically.
//    Method has return type and is called explicitly.

