// SECTION 13: QUIZ 1 (TRICK) - x = x
// ------------------------------------------------------------
class QuizOne {
    int x = 10;

    QuizOne(int x) {
        // Wrong assignment: parameter assigned to itself.
        x = x;
    }

    void display() {
        System.out.println(x);
    }

    public static void main(String[] args) {
        QuizOne t = new QuizOne(50);
        t.display();
        // Output: 10
        // Reason: instance variable x never changed.
    }
}

// Correct version for learning:
class QuizOneFixed {
    int x = 10;

    QuizOneFixed(int x) {
        this.x = x;
    }

    void display() {
        System.out.println(x);
    }
}

